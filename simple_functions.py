# Custom python functions


def double_number(a):
    """
    Doubles the number and returns the answer
    Args:
        a (int) : number
    Returns:
        int : Double the number
    """
    print(f'value before double_number(): {a}')
    return a+a

def square_number(a):
    """
    Squares the number and returns the answer
    Args:
        a (int) : number
    Returns:
        int : Squares the number
    """
    print(f'value before double_number(): {a}')
    return a*a